package mission5_221104.ex01Model;

public class Calculator {

    public Integer plus(int x, int y) {
        return x + y;
    }

    public Integer minus(int x, int y) {
        return x - y;
    }

    public Double product(int x, int y) {
        return (double)x * y;
    }

    public Double divide(int x, int y) {
        return (double)x / y;
    }
}
